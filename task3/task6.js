//task 6: Nested loops for multiplication table

//for 3*3 from 1 to 3

for (let i = 1; i <= 3; i++) {
  for (let j = 1; j <= 3; j++) {
    console.log(`${i} * ${j} = ${i * j}`);
  }
}

